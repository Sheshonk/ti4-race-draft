        public async Task<Dict<Guid>> Create(string[] names)
        {
            //TODO: bulk inserts
            var publicId = Guid.NewGuid();
            var game = await _gameRepo.CreateO(new Game() { PublicId = publicId });

            names = names.OrderBy(_ => (new Random()).Next()).ToArray();
            int firstPlayerId = -1;
            for (int x = 0; x < names.Length; x++)
            {
                var id = await _playerRepo.Create(new Player()
                {
                    AuthToken = null,
                    DraftOrder = x,
                    GameId = game.Id,
                    IsAdmin = false,
                    Name = names[x],
                });

                if (x == 0)
                    firstPlayerId = id;
            }

            game.CurrentPlayerId = firstPlayerId;
            await _gameRepo.Update(game);

            var races = await _raceRepo.Search().Select(_ => _.Id).ToListAsync();
            races = races.OrderBy(_ => (new Random()).Next()).ToList();
            for (int x = 0; x < races.Count; x++)
            {
                await _draftRepo.Create(new Draft()
                {
                    GameId = game.Id,
                    Order = x,
                    RaceId = races[x],
                    SuperFaction = x == 0 ? true : false
                });
            }

            var groupCount = 4;
            var adjectives = GetAdjectives().OrderBy(_ => (new Random()).Next()).Take(groupCount).ToList();
            var nouns = GetNouns().OrderBy(_ => (new Random()).Next()).Take(groupCount).ToList();
            for (int x = 0; x < groupCount; x++)
            {
                await _groupRepo.Create(new Group()
                {
                    GameId = game.Id,
                    Name = $"{adjectives[x][0].ToString().ToUpper()}{adjectives[x].AsSpan(1)} {nouns[x][0].ToString().ToUpper()}{nouns[x].AsSpan(1)}"
                });
            }

            return new Dict<Guid>() { Id = publicId };
        }

        public async Task<GameDetail> Get(Guid publicId, Guid authToken)
        {
            var game = _gameRepo.Search().Where(_ => _.PublicId == publicId).FirstOrDefault();
            if (game == null)
                return null;
            var players = _playerRepo.Search().Where(_ => _.GameId == game.Id).OrderBy(_ => _.DraftOrder).ToList();
            var races = _raceRepo.Search().Select(_ => new RaceDetail() { IconUrl = _.IconUrl, Id = _.Id, Name = _.Name, WikiUrl = _.WikiUrl}).ToList();
            var groups = _groupRepo.Search().Where(_ => _.GameId == game.Id).ToList();
            var drafts = _draftRepo.Search().Where(_ => _.GameId == game.Id).ToList();

            int? authPlayerId = players.Where(_ => _.AuthToken == authToken).Select(_ => (int?)_.Id).FirstOrDefault();

            return new GameDetail()
            {
                AuthPlayerId = authPlayerId,
                AdminId = players.Where(_ => _.IsAdmin == true).Select(_ => _.Id).FirstOrDefault(),
                Complete = game.Complete,
                CurrentPlayer = players.Where(_ => _.Id == game.CurrentPlayerId).Select(_ => new PlayerDetail { Id = _.Id, Name = _.Name, DraftOrder = _.DraftOrder, Claimable = _.AuthToken == null ? true : false }).FirstOrDefault(),
                Groups = groups.Select(groupp => new GroupDetail()
                {
                    Id = groupp.Id,
                    Name = groupp.Name,
                    Races = (
                        from race in races
                        join draft in drafts on race.Id equals draft.RaceId
                        where draft.GroupId == groupp.Id
                        select race
                    ).ToList(),
                    Winner = groupp.Winner
                }).ToList(),
                Hand = authPlayerId == null ? null : (
                    from race in races
                    join draft in drafts on race.Id equals draft.RaceId
                    where draft.PlayerId == authPlayerId
                        && draft.GroupId == null
                    select new RaceDetail()
                    {
                        DraftId = draft.Id,
                        IconUrl = race.IconUrl,
                        Id = race.Id,
                        Name = race.Name,
                        WikiUrl = race.WikiUrl
                    }
                ).ToList(),
                Id = game.Id,
                Players = players.Select(_ => new PlayerDetail { Id = _.Id, Name = _.Name, DraftOrder = _.DraftOrder, Claimable = _.AuthToken == null ? true : false }).ToList(),
                SuperFaction = !game.Complete ? null : (
                    from race in races
                    join draft in drafts on race.Id equals draft.RaceId
                    where draft.SuperFaction == true
                    select race
                ).FirstOrDefault()
            };
        }

