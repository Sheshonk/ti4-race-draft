        public GameService(IDbRepository<Draft> draftRepo, IDbRepository<Game> gameRepo, IDbRepository<Group> groupRepo, IDbRepository<Player> playerRepo, IDbRepository<Race> raceRepo)
        {
            _draftRepo = draftRepo;
